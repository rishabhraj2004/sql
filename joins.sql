-- LEFT JOIN ( LEFT TABLE KA SARA DATA SHOW KREGA AND RIGHT TABLE MAI JO BHI LEFT TABLE SE COMMON DATA HOGA SIRF UNHI COMMON DATA KO SHOW KREGA).

SELECT *
FROM STUDENTS AS A
LEFT JOIN COURSE AS C
ON A.ID = C.ID;



-- RIGHT JOIN    (RIGHT TABLE KA SARA DATA SHOW KREGA AND LEFT TABLE MAI JO BHI RIGJHT TABLE SE COMMON DATA HOGA SIRF UNHI COMMON DATA KO SHOW KREGA).

SELECT *
FROM STUDENTS AS A
RIGHT JOIN COURSE AS C
ON A.ID = C.ID;



