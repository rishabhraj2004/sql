-- LEFT EXCLUSIVE DATA ( LEFT TABLE KA SARA DATA DEGA AND JO DATA LEFT & RIGHT DATA MAI COMMON HOGA WO RSLT  MAI NHI DEGA)


SELECT *
FROM STUDENTS AS S
LEFT JOIN COURSE AS C
ON S.ID = C.ID
WHERE C.ID IS NULL;


-- RIGHT EXCLUSIVE DATA ( RIGHT TABLE KA SARA DATA DEGA AND JO DATA LEFT & RIGHT DATA MAI COMMON HOGA WO RSLT  MAI NHI DEGA)



SELECT *
FROM STUDENTS AS S
RIGHT JOIN COURSE AS C
ON S.ID = C.ID
WHERE S.ID IS NULL; 



